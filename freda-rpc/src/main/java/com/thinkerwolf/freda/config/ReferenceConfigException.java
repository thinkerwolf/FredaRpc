package com.thinkerwolf.freda.config;

public class ReferenceConfigException extends RuntimeException {

    /**
     *
     */
    private static final long serialVersionUID = -8122986308203392592L;

    public ReferenceConfigException() {
        super();
    }

    public ReferenceConfigException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ReferenceConfigException(String arg0) {
        super(arg0);
    }

    public ReferenceConfigException(Throwable arg0) {
        super(arg0);
    }

}
